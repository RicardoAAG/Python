"""
30. Write a Python program that will accept the base and height of a triangle and compute its area.
"""

base=int(input("Inserta la base del triangulo del cual se busca calcular su area: "))
altura=int(input("Inserta la altura del triangulo del cual se busca calcular su area: "))

print("\nArea:",(base*altura)/2)